public class OperatorDemo {
    public static void main(String[] args) {
        int a=89;
        int b=67;
        int sum = a+b;
        int diff = a-b;
        int mul = a*b;
        int divide = a/b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(divide);
    }
}
